MC1000.prototype.mc1000CoreGetPsg = function(mc1000Core) {
	if (this.psg == null) {
		this.masterChannel = new MasterChannel();
		this.psg = new AY38910(mc1000Core);
		this.psg.setMode(PsgDeviceChannel.MODE_SIGNED);
		this.psg.setDevice(PsgDeviceChannel.DEVICE_AY_3_8910);
		this.masterChannel.addChannel(this.psg);
		this.filter = new BiquadFilterChannel();
		this.looper = new AudioLooper(4096);
		this.looper.setChannel(this.filter);
		this.filter.setChannel(this.masterChannel);
	}
	return this.psg;
};

