MC1000.prototype.getBasicProgram = function() {
	var me = this; // 'this' refers to 'window' in the state functions. Use 'me' to keep reference to MC1000 object.
	var program = "";
	var i, nl, b, c;
	var state;
	
	var stateToken = function() {
		// Standard strategy.
		if (b >= 128 && b - 128 < MC1000.TOKENS.length) {
			// Token found: Output corresponding reserved word with padding spaces.
			var detokenized = MC1000.TOKENS[b - 128];
			program += " " + detokenized + " ";
			// Some reserved words imply a change in strategy.
			switch (detokenized) {
			case "DATA":
				state = stateDATA;
				break;
			case "REM":
			case "SAVE":
			case "LOAD" :
				state = stateChar;
				break;
			}
		} else {
			switch (c) {
				case " ":
					// Space found: Output hexadecimal notation so it is not lost.
					program += "~20";
					break;
				default:
					stateStringOrChar();
			}
		}
	};
	
	var stateDATA = function() {
		// Handles characters between data items in a DATA instruction: spaces, commas...
		stateStringOrChar();
		switch (c) {
		case " ":
		case ",":
			// Space or comma don't change the strategy.
			break;
		case ":":
			// End of DATA instruction.
			state = stateToken;
			break;
		default:
			// Data item found.
			state = stateDATA2;
		}
	};
	
	var stateDATA2 = function() {
		// Handles data item in DATA instruction.
		stateStringOrChar();
		switch (c) {
		case ",":
			// Back to handling characters between data items.
			state = stateDATA;
			break;
		case ":":
			// End of DATA instruction.
			state = stateToken;
			break;
		}
	};
	
	var stateStringOrChar = function() {
		// Identifies start of string.
		stateChar();
		switch (c) {
		case '"':
			// Start of string.
			// Saves current strategy to resume when string ends.
			stateBeforeString = state;
			state = stateString;
			break;
		}
	};
	
	var stateString = function() {
		// Handles string content until ending quotes are found.
		stateChar();
		switch (c) {
		case '"':
			// End of string.
			state = stateBeforeString;
			break;
		}
	};
	
	var stateChar = function() {
		// Handles characters.
		if (b >= 32 && b < 96) {
			// MC6847's ASCII character.
			program += c;
		} else if ((b ^ 0x80) >= 32 && (b ^ 0x80) < 96) {
			// MC6847's ASCII character in INVERSE video.
			// Output grave + normal character.
			program += "`" + String.fromCharCode(b ^ 0x80);
		} else {
			// Other characters.
			// Output as tilde + hexadecimal code.
			program += "~" + (b < 0x10 ? "0" : "") + b.toString(16).toUpperCase();
		}
	};

	// Get BASIC program start address at 0x349 (usually 0x3d5).
	i = this.ram.read(0x349) | (this.ram.read(0x34a) << 8);
	while (true) {
		// Get address of next BASIC line.
		nl = this.ram.read(i++) | (this.ram.read(i++) << 8);
		// 0x000 signals end of program.
		if (nl == 0) break;
		// Print line number.
		program += (this.ram.read(i++) | (this.ram.read(i++) << 8)) + " ";
		// Initial state.
		state = stateToken;
		while (true) {
			// Get next byte/character.
			b = this.ram.read(i++);
			c = String.fromCharCode(b);
			// Expected end-of-line situation.
			if (i >= nl && b == 0) break;
			// Process byte/character.
			state();
			// Abnormal end-of-line situation.
			if (i >= nl) break;
		}
		program += "\n";
		i = nl;
	}
	return program;
};

MC1000.prototype.setBasicProgram = function(program) {
	var me = this; // 'this' refers to 'window' in the state functions. Use 'me' to keep reference to MC1000 object.
	var basicst = this.ram.read(0x349) | (this.ram.read(0x34a) << 8);
	var lines, line;
	var k, linest;
	k = basicst;
	var i, j;
	var b, c;
	var state;
	
	var stateToken = function() {
		// Default strategy: Discards spaces and tokenizes reserved words.

		switch (c) {
		case " ":
			// Discards spaces.
			k--;
			break;
		default:
			// Token?
			var tokenFound = false;
			for (var l = 0; l < MC1000.TOKENS.length; l++) {
				var token = MC1000.TOKENS[l];
				if (line.substr(j, token.length) == token) {
					tokenFound = true;
					me.ram.write(k, l ^ 0x80);
					j += token.length - 1;
					break;
				}
			}
			if (tokenFound) {
				// Some reserved words imply a change in strategy.
				switch (token) {
				case "DATA":
					state = stateDATA;
					break;
				case "REM":
				case "SAVE":
				case "LOAD":
					state = stateREM;
					break;
				}
			} else {
				// Other characters.
				stateStringOrChar();
			}
		}
	};
	
	var stateDATA = function() {
		// Handles start of DATA instruction: Discards first space, if any.
		switch (c) {
		case " ":
			// Discards first space after "DATA" word.
			k--;
			state = stateDATA2;
			break;
		default:
			state = stateDATA2;
			stateDATA2();
			break;
		}
	};
	
	var stateDATA2 = function() {
		// Handles characters between items of data in DATA instruction: spaces, commas...
		stateChar();
		switch (c) {
		case " ":
		case ",":
			// Space or comma don't change the strategy.
			break;
		case ":":
			// End of DATA instruction.
			state = stateToken;
			break;
		default:
			// item of data found.
			state = stateDATA3;
			break;
		}
	};
	
	var stateDATA3 = function() {
		// Handles data item in DATA instruction.
		stateStringOrChar();
		switch (c) {
		case ",":
			// Back to handling characters between items of data.
			state = stateDATA2;
			break;
		case ":":
			// End of DATA instruction.
			state = stateToken;
			break;
		}
	};
	
	var stateREM = function() {
		// Handles start of REM/SAVE/LOAD instruction: Discards first space, if any.
		switch (c) {
		case " ":
			// Discards first space after REM/SAVE/LOAD.
			k--;
			break;
		default:
			state = stateChar;
			stateChar();
			break;
		}
	};
	
	var stateStringOrChar = function() {
		// Identifies start of string.
		stateChar();
		switch (c) {
		case '"':
			// Start of string.
			// Saves current strategy to resume when string ends.
			stateBeforeString = state;
			state = stateString;
			break;
		}
	};

	var stateString = function() {
		// Handles string content until quote is found.
		stateChar();
		switch (c) {
		case '"':
			// End of string.
			state = stateBeforeString;
			break;
		}
	};
	
	var stateChar = function() {
		// Handles characters.
		switch (c) {
		case "`":
			// Inverse video character notation: `X.
			if (j + 1 < line.length) {
				me.ram.write(k, line.charCodeAt(++j) ^ 0x80);
			}
			break;
		case "~":
			// Hexadecimal notation: ~XX.
			if (j + 2 < line.length) {
				var hex = line.substr(j + 1, 2);
				if (hex.match(/^[0-9a-fA-F]{2}$/)) {
					me.ram.write(k, parseInt(hex, 16));
					j += 2;
				}
			}
			break;
		}
	};
	
	lines = program.toUpperCase().split("\n");
	for (i = 0; i < lines.length; i++) {
		line = lines[i];
		var match = line.match(/^\s*(\d*)\s*(\D.*?)?\s*$/);
		if (match[1] || match[2]) {
			// Tokenized line consists of:
			// 2 bytes that point to the next line.
			// + 2 bytes containing line number.
			// + line content.
			// + 1 byte 0 at end of line.

			// Save line start position to be updated later with pointer to next line.
			linest = k;
			k += 2;
			
			// Line number.
			var lineno = parseInt(match[1]) || 0;
			this.ram.write(k++, lineno & 0xff);
			this.ram.write(k++, (lineno >> 8) & 0xff);
			
			// Line content.
			line = match[2] || "";
			
			j = 0;
			state = stateToken;
			
			while (j < line.length) {
				c = line.charAt(j);
				b = c.charCodeAt(0);
				this.ram.write(k, b);
				
				state();
				
				j++;
				k++;
			}
			// Byte 0 at end of line.
			this.ram.write(k++, 0);
			// Update pointer to next line.
			this.ram.write(linest, k & 0xff);
			this.ram.write(linest + 1, (k >> 8) & 0xff);
		}
	}
	// Output end of program.
	this.ram.write(k++, 0);
	this.ram.write(k++, 0);
	// Update system variable that points to end of program.
	this.ram.write(0x3b7, k & 0xff);
	this.ram.write(0x3b8, (k >> 8) & 0xff);
};

MC1000.prototype.getTextScreen = function() {
	var screen = "";
	for(var i = 0; i < 512; i++) {
		if((i & 31) == 0) {
			if(i != 0) {
				screen += "\n";
			}
		}
		screen += String.fromCharCode(this.vram.read(i));
	}
	return screen;
};

