function MC1000(mc1000HtmlDelegate) {
	this.delegate = mc1000HtmlDelegate;
	this.init();
}

MC1000.prototype.init = function() {
	this.z80 = null;
	this.vdg = null;
	this.looper = null;
	this.filter = null;
	this.masterChannel = null;
	this.psg = null;
	this.keyboard = null;
	this.ram = null;
	this.vram = null;
	this.rom = null;
	this.core = new MC1000Core(this);
};

MC1000.prototype.executeStart = function() {
	if (!this.core) {
		this.init();
	}
	this.core.executeStart();
};

MC1000.prototype.executeStop = function() {
	if (!this.core) {
		this.init();
	}
	this.core.executeStop();
};

MC1000.prototype.reset = function() {
	if (!this.core) {
		this.init();
	}
	this.core.reset();
};

